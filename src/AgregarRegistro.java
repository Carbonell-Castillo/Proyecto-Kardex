    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        Registro registro = new Registro();
        boolean correcto = false;
        int pos=0;
        try{
            pos= Integer.parseInt(txtPos.getText());
            //Datos generales
            registro.setCodigoRegistro(Integer.parseInt(txtCodigoRegistro.getText()));
            registro.setCodigoArticulo(Integer.parseInt(txtCodigoAriiculo.getText()));
            

            //Datos kardex
            registro.setFecha(txtFecha.getText());
            registro.setDescripcion(txtDescripcion.getText());
            registro.setPrecioUnitario(Float.parseFloat(txtPrecioUnitario.getText()));
            registro.setExistencia(Integer.parseInt(txtExistencia.getText()));
            registro.setTipo((String) cboTipo.getSelectedItem());
            correcto = true;
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "DATOS INVÁLIDOS");
        }
        if(correcto){
            SG.registros.modificarRegistro((pos-1), registro);
            JOptionPane.showMessageDialog(null, "Producto Modificado #"+pos);
            limpiarTextos();
            txtCodigoRegistro.setText("");
        }
    }//GEN-LAST:event_btnModificarActionPerformed
    public void limpiarTextos(){
        txtFecha.setText("");
        txtDescripcion.setText("");
        txtPrecioUnitario.setText("");
        txtExistencia.setText("");

    }
    public void guardarDatos(){
           Registro registro = new Registro();
        boolean correcto = false;
        try{
            //Datos generales
            registro.setCodigoRegistro(Integer.parseInt(txtCodigoRegistro.getText()));
            registro.setCodigoArticulo(Integer.parseInt(txtCodigoAriiculo.getText()));
            //Datos kardex
            registro.setFecha(txtFecha.getText());
            registro.setDescripcion(txtDescripcion.getText());
            registro.setPrecioUnitario(Float.parseFloat(txtPrecioUnitario.getText()));
            registro.setExistencia(Integer.parseInt(txtExistencia.getText()));
            registro.setTipo((String) cboTipo.getSelectedItem());
            correcto = true;
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Datos invalidos, por favor llene los campos");
        }
        if(correcto){
            SG.registros.addRegistro(registro);
            JOptionPane.showMessageDialog(null, "Producto agregago #"+SG.registros.getTamaño());
            limpiarTextos();
            txtCodigoRegistro.setText("");
        }
        MovimientoAnterior();
      }
    
    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if(cboTipo.getSelectedItem().equals("Salida")){
            if(SG.registros.getTamaño()>0){
                guardarDatos();
            }else {
                JOptionPane.showMessageDialog(null, "No tiene entradas registradas No de entradas: #"+SG.registros.getTamaño());
                limpiarTextos();
            }
        }else{
            guardarDatos();
        }
        
        
    }//GEN-LAST:event_btnGuardarActionPerformed

