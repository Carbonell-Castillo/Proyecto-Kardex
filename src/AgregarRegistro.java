    public void inicializarDatos(int codigoArticulo){        
        Producto producto = SG.productos.getProductoCodigo(codigoArticulo);
        txtCodigoAriiculo.setText(String.valueOf(codigoArticulo));
        txtArticulo.setText(String.valueOf(producto.getArticulo()));
        MovimientoAnterior();
    }
    
    public void MovimientoAnterior(){
        int codigoArticulo= Integer.parseInt(txtCodigoAriiculo.getText());
        if(SG.registros.buscarRegistroCodigoProducto(codigoArticulo).size()>0){
        Registro registro = SG.registros.getRegistroMovimiento(codigoArticulo);
        txtCodigoProductoAnterior.setText(String.valueOf(registro.getCodigoArticulo()));
        txtCodigoRegistroAnterior.setText(String.valueOf(registro.getCodigoRegistro()));
        txtDescripcionAnterior.setText(String.valueOf(registro.getDescripcion()));
        txtExistenciaAnterior.setText(String.valueOf(registro.getExistencia()));
        txtPrecioUnitarioAnterior.setText(String.valueOf(registro.getPrecioUnitario()));
        }
        
        
    }
